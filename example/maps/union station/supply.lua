mapfunctions:setlightintensity(obj, 10)
mapfunctions:setlightcolor(obj, 0.45, 0.1, 0.3, 1)
mapfunctions:destroyobj(cube)
mapfunctions:destroyobj(plane)
mapfunctions:spawnlocation(0, 0, 0, 0, 0, 3, mapname, mapname)
